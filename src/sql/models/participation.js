import { DataTypes } from 'sequelize';
import { nanoid } from 'nanoid';

export default {
  id: {
    type: DataTypes.STRING(21),
    allowNull: false,
    defaultValue: nanoid,
    unique: true,
    primaryKey: true
  },
  message: DataTypes.TEXT,
  reason: DataTypes.STRING,
  status: DataTypes.INTEGER
};
